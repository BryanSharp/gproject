public final class C0118a {
    public static float m231a(float f, float f2, float f3, float f4) {
        return (float) Math.hypot((double) (f3 - f), (double) (f4 - f2));
    }

    public static float m230a(float f, float f2, float f3) {
        return ((1.0f - f3) * f) + (f3 * f2);
    }
}
